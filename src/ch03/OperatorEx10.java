package ch03;

public class OperatorEx10 {
    public static void main(String[] args) {
        int x = 10;
        int y = 8;
        System.out.println(x + " 나누기 " + y + "의 몫은 " + x/y + "이고 나머지는 " + x % y + "이다.");

        System.out.println(-10 % 8);
        System.out.println(10 % -8);
        System.out.println(-10 % -8);
    }
}
